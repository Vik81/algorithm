print('Побитовое "И" числе 5 и 6 = {}'.format(5 & 6))
print('Побитовое "ИЛИ" числе 5 и 6 = {}'.format(5 | 6))
print('Побитовое "Исключительное ИЛИ" числе 5 и 6 = {}'.format(5 ^ 6))
print('Побитовый сдвиг вправо числа 5 на два знака = {}'.format(5 >> 2))
print('Побитовый сдвиг влево числа 5 на два знака = {}'.format(5 << 2))